package com.masai.a7july_recycler_view_1_animal_details_you2;

public class Menu {
    private int image;
    private String type;
    private String sound;

    public Menu(int image, String type, String sound) {
        this.image = image;
        this.type = type;
        this.sound = sound;
    }

    public int getImage() {
        return image;
    }

    public String getType() {
        return type;
    }

    public String getSound() {
        return sound;
    }
}

